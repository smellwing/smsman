namespace App\Controller;

use Cake\Core\Configure;
use Cake\Network\Exception\ForbiddenException;
use Cake\Network\Exception\NotFoundException;
use Cake\View\Exception\MissingTemplateException;
use Cake\ORM\TableRegistry;
use App\Controller\AppController;
use Cake\Event\Event;

/**
 * Static content controller
 *
 * This controller will render views from Template/Pages/
 *
 * @link http://book.cakephp.org/3.0/en/controllers/pages-controller.html
 */
class PagesController extends AppController
{

	public function beforeFilter(Event $event)
	{
//		parent::beforeFilter($event);
		// Allow users to register and logout.
		// You should not add the "login" action to allow list. Doing so would
		// cause problems with normal functioning of AuthComponent.
		$this->Auth->allow(['display']);
	}
    /**
     * Displays a view
     *
     * @param string ...$path Path segments.
     * @return void|\Cake\Network\Response
     * @throws \Cake\Network\Exception\ForbiddenException When a directory traversal attempt.
     * @throws \Cake\Network\Exception\NotFoundException When the view file could not
     *   be found or \Cake\View\Exception\MissingTemplateException in debug mode.
     */
    public function display(...$path)
    {
        if(!empty($this->Auth->user())) return $this->redirect($this->Auth->redirectUrl());
    	$this->Tasks = TableRegistry::get('Tasks');
    	//$cargaPhoneSMS = $this->Tasks->cargaListasActivaPhoneSMS();
    	$cm = $this->Tasks->getCargaMaxima();
    	//$time = $cargaPhoneSMS/$cm;
    	//$cargaHoraActiva = sprintf('%02d:%02d', (int) $time, fmod($time, 1) * 60);
    	$cargaHoraActiva = null;
        $count = count($path);
        if (!$count) {
            return $this->redirect('/');
        }
        if (in_array('..', $path, true) || in_array('.', $path, true)) {
            throw new ForbiddenException();
        }
        $page = $subpage = null;

        if (!empty($path[0])) {
            $page = $path[0];
        }
        if (!empty($path[1])) {
            $subpage = $path[1];
        }
        $this->set(compact('page', 'subpage','cargaHoraActiva'));
        
        if ($this->request->is('post')) {
        	$user = $this->Auth->identify();
        	if ($user) {
        		$this->Auth->setUser($user);
        		return $this->redirect($this->Auth->redirectUrl());
        	}
        	$this->Flash->error(__('Nombre de usuario o clave incorrecta.'));
        }
        
        try {
            $this->render(implode('/', $path));
        } catch (MissingTemplateException $e) {
            if (Configure::read('debug')) {
                throw $e;
            }
            throw new NotFoundException();
        }
    }
    
    
}
